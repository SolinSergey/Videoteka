CREATE TABLE countries
(
    id           bigserial NOT NULL PRIMARY KEY,
    title        varchar   NOT NULL,
    created_by   varchar NULL,
    created_when timestamp NULL,
    update_by    varchar NULL,
    update_when  timestamp NULL,
    is_deleted   bool NULL DEFAULT false,
    deleted_by   varchar NULL,
    deleted_when timestamp NULL
);

CREATE TABLE directors
(
    id           bigserial NOT NULL PRIMARY KEY,
    first_name   varchar   NOT NULL,
    last_name    varchar   NOT NULL,
    created_by   varchar NULL,
    created_when timestamp NULL,
    update_by    varchar NULL,
    update_when  timestamp NULL,
    is_deleted   bool NULL DEFAULT false,
    deleted_by   varchar NULL,
    deleted_when timestamp NULL
);

CREATE TABLE films
(
    id             bigserial NOT NULL PRIMARY KEY,
    title          varchar   NOT NULL,
    premier_year   int4      NOT NULL,
    description    text NULL,
    created_by     varchar NULL,
    created_when   timestamp NULL,
    update_by      varchar NULL,
    update_when    timestamp NULL,
    is_deleted     bool NULL DEFAULT false,
    deleted_by     varchar NULL,
    deleted_when   timestamp NULL,
    image_url_link varchar NULL
);

CREATE TABLE genres
(
    id           bigserial NOT NULL PRIMARY KEY,
    title        varchar   NOT NULL,
    created_by   varchar NULL,
    created_when timestamp NULL,
    update_by    varchar NULL,
    update_when  timestamp NULL,
    is_deleted   bool NULL DEFAULT false,
    deleted_by   varchar NULL,
    deleted_when timestamp NULL
);

CREATE TABLE countries_films
(
    country_id int8 NOT NULL,
    film_id    int8 NOT NULL,
    CONSTRAINT countries_films_pk PRIMARY KEY (country_id, film_id),
    CONSTRAINT countries_films_fk FOREIGN KEY (film_id) REFERENCES films (id),
    CONSTRAINT countries_films_fk_1 FOREIGN KEY (country_id) REFERENCES countries (id)
);

CREATE TABLE directors_films
(
    film_id     int8 NOT NULL,
    director_id int8 NOT NULL,
    CONSTRAINT directors_fillms_pk PRIMARY KEY (film_id, director_id),
    CONSTRAINT directors_fillms_fk FOREIGN KEY (film_id) REFERENCES films (id),
    CONSTRAINT directors_fillms_fk_1 FOREIGN KEY (director_id) REFERENCES directors (id)
);

CREATE TABLE genres_films
(
    film_id  int8 NOT NULL,
    genre_id int8 NOT NULL,
    CONSTRAINT genres_films_pk PRIMARY KEY (film_id, genre_id),
    CONSTRAINT genres_films_fk FOREIGN KEY (film_id) REFERENCES films (id),
    CONSTRAINT genres_films_fk_1 FOREIGN KEY (genre_id) REFERENCES genres (id)
);

CREATE TABLE prices
(
    id           bigserial NOT NULL,
    film_id      int8      NOT NULL,
    price_rent   int4 NULL,
    price_sale   int4 NULL,
    created_by   varchar NULL,
    created_when timestamp NULL,
    update_by    varchar NULL,
    update_when  timestamp NULL,
    is_deleted   bool NULL DEFAULT false,
    deleted_by   varchar NULL,
    deleted_when timestamp NULL,
    CONSTRAINT prices_pk PRIMARY KEY (id),
    CONSTRAINT prices_fk FOREIGN KEY (film_id) REFERENCES films (id)
);

CREATE TABLE grades_reviews
(
    id           bigserial NOT NULL,
    created_by   varchar NULL,
    created_when timestamp NULL,
    deleted_by   varchar NULL,
    deleted_when timestamp NULL,
    is_deleted   bool NULL DEFAULT false,
    update_by    varchar NULL,
    update_when  timestamp NULL,
    grade        int4      NOT NULL,
    review       text NULL,
    user_id      int8      NOT NULL,
    film_id      int8      NOT NULL,
    is_moderate  bool NULL DEFAULT false,
    CONSTRAINT grades_pk PRIMARY KEY (id),
    CONSTRAINT grades_fk FOREIGN KEY (film_id) REFERENCES films (id)
);

